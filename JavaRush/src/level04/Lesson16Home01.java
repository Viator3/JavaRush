package level04;
/* I will never work for a penny
Using the cycle to display a hundred times the inscription:
"I will never work for a penny. Amigo.»
*/

public class Lesson16Home01 {
	public static void main(String[] args)
    {
        for (int num = 0; num < 100; num++) {
            System.out.println("I will never work for a penny. Amigo.");
        }
    }
}
