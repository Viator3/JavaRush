package level01;
/* JavaRush. Learn once-anywhere anywhere
Write a program that displays the inscription: "JavaRush. Learn once-use anywhere" 10 times.
*/

public class Lesson04Task02 {

	public static void main(String[] args) {
		print5Times("JavaRush. Learn once - use anywhere");
        print5Times("JavaRush. Learn once - use anywhere");
	}


	public static void print5Times(String s) {
    	System.out.println(s);
        System.out.println(s);
        System.out.println(s);
        System.out.println(s);
        System.out.println(s);
    }

}
