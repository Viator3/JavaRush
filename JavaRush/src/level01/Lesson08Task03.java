package level01;
/* Solve problems
Write a program that displays the inscription on the screen: "If you want - solve problems, 
do not want to - solve reluctantly" 16 times.
*/

public class Lesson08Task03 {

	public static void main(String[] args) {
		print4Times("If you want - solve problems, do not want to - solve reluctantly");
        print4Times("If you want - solve problems, do not want to - solve reluctantly");
        print4Times("If you want - solve problems, do not want to - solve reluctantly");
        print4Times("If you want - solve problems, do not want to - solve reluctantly");
	}
	
	public static void print4Times(String s)
    {
        System.out.println(s);
        System.out.println(s);
        System.out.println(s);
        System.out.println(s);
    }

}
